function lvl_to_name(level){
    let lvl = parseInt(level);
    console.log(lvl);
    switch (lvl) {
        case 0:
            return "Beginner";
            break;
        case 1:
            return "Neophyte";
            break;
        case 2:
            return "Initiate";
            break;
        case 3:
            return "Practitioner";
            break;
        case 4:
            return "Expert";
            break;
        case 5:
            return "Master";
            break;
    }
return "GOD";
}



